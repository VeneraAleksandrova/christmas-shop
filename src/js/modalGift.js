const modal_gift = document.querySelector(".modal-gift");

function renderModalGift({ name, description, category, superpowers }) {
  modal_gift.style.display = "flex";

  modal_gift.innerHTML = `
                  <div class="gift-wrapper">
                      <img src="./src/img/gifts/${category
                        .slice(4)
                        .toLowerCase()}.png" alt="gift">
                                          <div class="gift-close"></div>
                      <div class="gift__title gift__title_modal">
                          <h4 class=${category
                            .slice(4)
                            .toLowerCase()}>${category}</h4>
                          <p class="gift-name">${name}</p>
                          <p class="gift-desc">${description}</p>
                          <h4>Adds superpowers to:</h4>
                          <div class="superpowers-block">
                          ${addSuperpowers(superpowers)}
                          </div>
                      </div>
              </div>`;

  document
    .querySelector(".gift-close")
    .addEventListener("click", () => closeModalGift(event));
  modal_gift.addEventListener("click", () => closeModalGift(event));
}

function closeModalGift(e) {
  if (
    e.target.classList.contains("gift-close") ||
    (e.target.classList.contains("modal-gift") &&
      !e.target.classList.contains("gift-wrapper"))
  ) {
    const clientWidth = document.documentElement.clientWidth;

    modal_gift.innerHTML = ``;
    modal_gift.style.display = "none";
    activeBodyHidden(false);
    hiddenScrollRight(document.documentElement.clientWidth, clientWidth);
  }
}

function addSuperpowers(powers) {
  let template = ``;
  Object.entries(powers).forEach(function ([key, value]) {
    template += renderSuperpowers(key, value);
  });
  return template;
}

function renderSuperpowers(key, value) {
  return `
     <div class="superpowers-item">
      <span>${key[0].toUpperCase() + key.slice(1)}</span>
      <span>${value}</span>
     <div>${addColoredSnowflake(value)}</div>
     </div>`;
}

